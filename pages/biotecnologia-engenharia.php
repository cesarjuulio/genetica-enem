        <h1>O que é Biotecnologia e Engenharia Genética?</h1>
        <p>Biotecnologia é o uso de organismos vivos ou de componentes deles (como enzimas e DNA) para desenvolver produtos e soluções tecnológicas úteis ao ser humano. Ela une biologia, química, genética e outras áreas para aplicações na medicina, agricultura, indústria e meio ambiente. 
            Engenharia genética é um ramo da biotecnologia que manipula diretamente os genes de um organismo.
            Ela permite modificar o DNA de seres vivos para que apresentem características específicas — como plantas mais resistentes a pragas ou bactérias que produzem medicamentos.</p>
        
        <h2>Aplicações da Biotecnologia</h2>
        <ul>
            <li>Medicina:</li>
            <ol>Produção de insulina humana por bactérias modificadas.</ol>
            <lo>Desenvolvimento de vacinas e terapias gênicas.</lo>
            <lo>Diagnóstico mais rápido de doenças genéticas.</lo>
            <li>Agricultura:</li>
            <lo>Criação de plantas transgênicas (ex: milho resistente a insetos).</lo>
            <lo>Aumento da produtividade e da resistência às pragas.</lo>
            <li>Meio ambiente:</li>
            <lo>Uso de microrganismos para limpar áreas poluídas (biorremediação).</lo>
            <lo>Tratamento de esgoto com bactérias.</lo>
            <li>Indústria:</li>
            <lo>Produção de alimentos (ex: iogurtes, queijos) usando microrganismos.</lo>
            <lo>Desenvolvimento de biocombustíveis.</lo>
        </ul>

        <h2>Implicações Éticas</h2>
        <p>A manipulação genética levanta questões éticas importantes, como a segurança dos organismos geneticamente modificados, o impacto na biodiversidade e as implicações para a saúde humana. É fundamental que a sociedade discuta e regule essas práticas para garantir que sejam realizadas de forma responsável.
            <ul>
            <ol>Manipulação genética em humanos pode gerar desigualdades e levanta dúvidas sobre os limites éticos.</ol>
            <ol>Organismos transgênicos causam preocupações com impactos ambientais e à saúde.</ol>
            <ol>Patentes de biotecnologia, como sementes modificadas, podem prejudicar pequenos produtores.</ol>
            <ol>Testes em animais envolvem debates sobre sofrimento animal e direitos dos seres vivos.</ol>
        </ul></p>
