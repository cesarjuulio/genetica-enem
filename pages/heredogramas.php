        <h1>Heredogramas</h1>
        <p>Os heredogramas são diagramas que representam a herança de características genéticas em uma família. Eles são ferramentas valiosas para entender como as doenças genéticas são transmitidas de geração para geração.</p>
        
        <h2>O que é um Heredograma?</h2>
        <p>Um heredograma (também chamado de genealogia ou árvore genealógica) é uma representação gráfica que mostra as relações de parentesco entre os membros de uma família, indicando a transmissão de características genéticas (como doenças ou traços hereditários).</p>
        
        <h2>Como interpretar um Heredograma</h2>
        <p>Os heredogramas utilizam símbolos específicos para representar homens, mulheres, afetados por uma condição genética e não afetados. A interpretação correta é essencial para entender a herança de características e doenças.
            <ul> 
                <ol>Os homens são representados por quadrados (⬜).</ol>
                <ol>As mulheres são representadas por círculos (⚪).</ol>
                <ol>Indivíduos afetados (que possuem a característica ou doença estudada) são representados com a forma geométrica preenchida (⬛ ou ⚫).</ol>
                <ol>As linhas horizontais ligam casais.</ol>
                <ol>As linhas verticais conectam pais aos filhos.</ol>
                <ol>A ordem dos filhos é da esquerda para a direita, conforme nascem.</ol>
                <ol>Números romanos indicam as gerações (I, II, III...).</ol>
                <ol>Às vezes, há números embaixo dos indivíduos para identificá-los (ex: II-3 é o terceiro filho da segunda geração).</ol>   
            </ul>
        </p>
        
        <h2>Utilidades dos Heredogramas</h2>
        <ul>
            <li>Identificar o tipo de herança genética (dominante, recessiva, autossômica ou ligada ao sexo).</li>
            <li>Rastrear a presença de doenças genéticas em uma família.</li>
            <li>Auxiliar no diagnóstico médico e no aconselhamento genético.</li>
            <li>Entender se alguém é portador de um gene, mesmo sem manifestar a característica.</li>
            <li>Planejar a probabilidade de um casal ter filhos afetados.</li>
        </ul>
