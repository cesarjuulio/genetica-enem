        <h1>Heredogramas</h1>
        <p>Os heredogramas são diagramas que representam a herança de características genéticas em uma família. Eles são ferramentas valiosas para entender como as doenças genéticas são transmitidas de geração para geração.</p>
        
        <h2>O que é um Heredograma?</h2>
        <p>Um heredograma é uma representação gráfica que mostra a relação entre membros de uma família e a transmissão de características genéticas. Cada símbolo no heredograma representa um indivíduo, e as linhas conectando os símbolos indicam relações familiares.</p>
        
        <h2>Como interpretar um Heredograma</h2>
        <p>Os heredogramas utilizam símbolos específicos para representar homens, mulheres, afetados por uma condição genética e não afetados. A interpretação correta é essencial para entender a herança de características e doenças.</p>
        
        <h2>Utilidades dos Heredogramas</h2>
        <ul>
            <li>Identificar padrões de herança em famílias.</li>
            <li>Ajudar no diagnóstico de doenças genéticas.</li>
            <li>Auxiliar em aconselhamentos genéticos.</li>
        </ul>
