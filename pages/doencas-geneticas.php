        <h1>Doenças Genéticas</h1>
        <p>Doenças genéticas são alterações no material genético (DNA) que causam problemas no funcionamento do organismo. Elas podem ser hereditárias (passadas de pais para filhos) ou espontâneas (decorrentes de mutações que ocorrem ao acaso).</p>
        
        <h2>Causas das Doenças Genéticas</h2>
        <p>As doenças genéticas podem ser causadas por:</p>
        <ul>
            <li>Mutações em um único gene (doenças monogênicas)</li>
            <li>Alterações em múltiplos genes (doenças poligênicas)</li>
            <li>Alterações no número ou estrutura dos cromossomos</li>
            <li>Fatores hereditários: quando os genes alterados são transmitidos pelos pais</li>
            <li>Agentes externos: como radiação, substâncias químicas ou vírus que podem provocar mutações no DNA</li>
        </ul>

        <h2>Exemplos de Doenças Genéticas</h2>
        <p>Alguns exemplos de doenças genéticas incluem:</p>
        <ul>
            <li>Fibrose cística: doença hereditária que afeta os pulmões e o sistema digestivo.</li>
            <li>Distrofia muscular de Duchenne: degeneração progressiva dos músculos.</li>
            <li>Anemia falciforme: alteração nos glóbulos vermelhos do sangue.</li>
            <li>Síndrome de Down: alteração no cromossomo 21 (trissomia).</li>
            <li>Hemofilia: distúrbio de coagulação sanguínea hereditário.</li>
        </ul>

        <h2>Importância do Diagnóstico Genético</h2>
        <p>O diagnóstico genético é crucial para identificar doenças genéticas, permitindo intervenções precoces e aconselhamento familiar.</p>
