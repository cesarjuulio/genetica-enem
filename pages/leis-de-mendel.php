        <p>As leis de Mendel são princípios fundamentais da hereditariedade, formulados por Gregor Mendel no século XIX. Elas descrevem como os traços são transmitidos de uma geração para outra.</p>
        
        <h2>Primeira Lei - Lei da Segregação</h2>
        <p>Esta lei afirma que os alelos para um determinado gene se separam durante a formação dos gametas. Cada gameta recebe apenas um alelo de cada par.</p>
        
        <h2>Segunda Lei - Lei da Distribuição Independente</h2>
        <p>Esta lei estabelece que a distribuição de alelos de diferentes genes é independente uns dos outros durante a formação dos gametas.</p>
        
