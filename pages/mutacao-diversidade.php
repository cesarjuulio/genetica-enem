        <h1>Mutações e Diversidade Genética</h1>
        <p>Mutações são alterações na sequência do DNA que ocorrem de forma espontânea ou devido a fatores externos como radiação, produtos químicos ou vírus. Elas podem ocorrer em genes (mutações gênicas) ou em cromossomos (mutações cromossômicas). A diversidade genética é a variação do material genético entre os indivíduos de uma mesma espécie.</p>
        
        <h2>Tipos de Mutações</h2>
        <ul>
            <li><strong>Mutações Pontuais:</strong> Alterações em uma única base do DNA.</li>
            <li><strong>Inserções e Deleções:</strong> Adição ou remoção de nucleotídeos na sequência do DNA.</li>
            <li><strong>Mutações Silenciosas:</strong> Não alteram a proteína resultante.</li>
            <li><strong>Mutações Missense:</strong> Alteram um aminoácido na proteína.</li>
            <li><strong>Mutações Nonsense:</strong> Criam um códon de parada prematuro.</li>
        </ul>

        <h2>Impacto das Mutações na Diversidade Genética</h2>
        <p>As mutações são uma das principais fontes de variabilidade genética, que é essencial para a adaptação e evolução das espécies. Elas podem levar à formação de novas características que podem ser vantajosas, neutras ou prejudiciais para os organismos.</p>

        <h2>Exemplos de Mutações e suas Consequências</h2>
        <p>Algumas mutações podem resultar em doenças genéticas, enquanto outras podem conferir resistência a doenças ou adaptações a ambientes específicos.</p>
