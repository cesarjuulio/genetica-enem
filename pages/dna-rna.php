        <h1>DNA e RNA</h1>
        
        <h2>O que é DNA?</h2>
        <p>O DNA (ácido desoxirribonucleico) é uma molécula presente no núcleo das células que contém toda a informação genética dos seres vivos.
        É responsável por armazenar e transmitir as instruções necessárias para o desenvolvimento, funcionamento e reprodução dos organismos.
        Sua estrutura é formada por duas fitas em espiral, chamadas de dupla hélice.</p>
        
        <h2>O que é RNA?</h2>
        <p>O RNA (ácido ribonucleico) é uma molécula que atua na síntese de proteínas, sendo essencial para a expressão do código genético contido no DNA.
        Ele é formado por uma única fita (ao contrário do DNA) e pode sair do núcleo celular para o citoplasma, onde comanda a produção de proteínas.</p>
        
        <h2>Diferenças entre DNA e RNA</h2>
        <ul>
            <li>O DNA contém a informação genética, enquanto o RNA é responsável por traduzir essa informação em proteínas.</li>
            <li>O DNA possui desoxirribose como açúcar, enquanto o RNA possui ribose.</li>
            <li>O DNA é geralmente de dupla hélice, enquanto o RNA é de cadeia simples.</li>
        </ul>
        
        <h2>Importância Biológica</h2>
        <ul>
            <li>O DNA é essencial para a hereditariedade, ou seja, para a transmissão de características genéticas de uma geração para outra.</li>
            <li>O RNA é indispensável na produção de proteínas, que são fundamentais para todas as funções celulares.</li>
            <li>Juntos, DNA e RNA garantem o funcionamento correto das células e o controle da atividade biológica dos organismos.</li>
        </ul>
